function Resposta1(){
    let res1 = document.querySelector("#resposta1").value

    switch (res1.toLowerCase()){
        case "a":
            document.querySelector("#aviso1").innerHTML="Resposta errada."
            break
        case "b":
            document.querySelector("#aviso1").innerHTML="Certa resposta!"
            break
        case "c":
            document.querySelector("#aviso1").innerHTML="Resposta errada."
            break
        default:
            document.querySelector("#aviso1").innerHTML="Não temos essa alternativa."
    }
}

